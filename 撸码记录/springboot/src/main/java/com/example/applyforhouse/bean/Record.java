package com.example.applyforhouse.bean;
/**
 * 登陆类
 * @author 王恒
 *
 */
public class Record {
	private int id;
	private String areaSelect;
	private String computerName;
	private String computerCompany;
	private String name;
	private String phone;
	private String company;
	private String reason;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getAreaSelect() {
		return areaSelect;
	}

	public void setAreaSelect(String areaSelect) {
		this.areaSelect = areaSelect;
	}

	public String getComputerName() {
		return computerName;
	}

	public void setComputerName(String computerName) {
		this.computerName = computerName;
	}

	public String getComputerCompany() {
		return computerCompany;
	}

	public void setComputerCompany(String computerCompany) {
		this.computerCompany = computerCompany;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}
}
